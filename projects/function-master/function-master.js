function capitalizeAllWords(string) {
        //First we take is to split the sentence into an array of words so we can manipulate each word individually

  const words = string.split(" ");

  //The next step is to loop over the array of words and capitalize the first letter of each word.

  for (let i = 0; i < words.length; i++) {
    words[i] = words[i][0].toUpperCase() + words[i].substr(1);
  }

  //The last step is to join all the words to form a sentence using join so we can use to return an array as a string

  return words.join(" ");
}

